// Program to check if a number is even or odd
#include <iostream>
using namespace std;
int main(){
    int n;
    cout<<"Enter any number"<<endl;
    cin>>n;
    if (n%2==0)
    {
        cout<<"The number entered is even.";
    }
    else
    {
        cout<<"The number entered is odd";
    }
    return 0;
    
}